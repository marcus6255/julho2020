digite = input("Digite alguma coisa: ")
print("A palavra que você digitou é ", type(digite))
print("Somente espaços: ", digite.isspace())
print("É número:", digite.isnumeric())
print("É alfabético: ", digite.isalpha())
print ("É alfanu´merico", digite.isalnum())
print("Está em maiúscula: ", digite.isupper())
print("Está em minúscula: ", digite.islower())





